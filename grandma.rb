def speak_to_grandma(words)

  if words == words.upcase

    puts "NO, NOT SINCE 1938!"
    speak_to_grandma = "NO, NOT SINCE 1938!"
    speak_to_grandma
    else
      puts "HUH?! SPEAK UP, SONNY!"
      speak_to_grandma = "HUH?! SPEAK UP, SONNY!"
      speak_to_grandma
    end

end

