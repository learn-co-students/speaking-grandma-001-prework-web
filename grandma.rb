def speak_to_grandma(phrase)

  response1 = "HUH?! SPEAK UP, SONNY!"
  response2 = "NO, NOT SINCE 1938!"
  
  if phrase != "#{phrase}".upcase
    response1
  elsif phrase != "#{phrase}".upcase
    response1
  elsif phrase == "#{phrase}".upcase
    response2
  else phrase == "#{phrase}".upcase
    response2
  end

end